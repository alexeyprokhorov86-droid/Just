"""
Промпт-билдеры для анализа вложений с anti-hallucination guardrails.

Ключевое отличие от старого bot.build_analysis_prompt: контекст чата подаётся
МЯГКО (справочно), а не жёстко («отвечай на вопрос обсуждения»), плюс явный
запрет додумывания цифр/дат/имён. Это именно та проблема, которая дала
галлюцинацию на УПД id=464 (придуманные «№49 от 15.04», «сливки 11147 кг»,
«1 053 462 руб»).
"""
from __future__ import annotations

ANTI_HALLUCINATION_HEADER = """
КРИТИЧНО — АНТИ-ВЫДУМЫВАНИЕ:
- Пиши ТОЛЬКО то, что явно видно/извлечено из документа.
- Если номер документа, даты, контрагенты, суммы, позиции, количества НЕ ВИДНЫ
  или видны нечётко (плохое качество скана, скрытый текст, урезанные страницы) —
  честно пиши «не удалось извлечь <поле>» или «не видно на странице». Не гадай.
- Контекст обсуждения в чате — это СПРАВКА, НЕ ИСТОЧНИК ДАННЫХ. Никогда не
  переноси цифры или имена из обсуждения в описание документа.
- Если вопрос коллеги в обсуждении относится к документу — ответь на него только
  если ответ явно в документе; иначе скажи «не могу ответить из документа».
"""


def build_analysis_prompt(
    *,
    company_profile: str,
    doc_type: str,
    doc_content: str,
    chat_context: str,
    filename: str,
) -> str:
    """Основной промпт для LLM-анализа вложения.

    doc_content — либо реальный извлечённый текст, либо маркер "[PDF из N
    страниц прикреплён]" для Vision-режима.
    """
    parts: list[str] = []
    parts.append(company_profile)
    parts.append("")
    parts.append("Ты участник рабочего чата компании. Коллега прислал документ — дай краткий анализ.")
    parts.append(ANTI_HALLUCINATION_HEADER)
    parts.append("")
    parts.append("ФОРМАТ ОТВЕТА:")
    parts.append("- Сплошной текст, как сообщение коллеге. Никаких Markdown-заголовков, буллетов, таблиц.")
    parts.append("- Простое фото — 3-5 предложений; накладная/счёт — 5-7; многостраничный документ — до 15.")
    parts.append("- Начни с главного: что это и зачем. Укажи реквизиты которые ВИДНЫ в документе (номер, дата, контрагенты, итоговые суммы).")
    parts.append("- Если нужно действие — скажи кратко в конце.")
    parts.append("")

    if chat_context:
        parts.append("Справочный контекст обсуждения (для понимания зачем прислали документ, НЕ ИСТОЧНИК данных):")
        parts.append(chat_context)
        parts.append("")

    parts.append(f"Тип: {doc_type}")
    parts.append(f"Файл: {filename}")
    if doc_content and doc_content != "[Vision]":
        parts.append("")
        parts.append("Извлечённое содержимое:")
        parts.append(doc_content)
    parts.append("")
    parts.append("Краткий анализ (только факты из документа):")
    return "\n".join(parts)
